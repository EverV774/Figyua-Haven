    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        String n = txtNombre.getText();
        String p = txtPrecio.getText();
        Clases.CProductos objP = new Clases.CProductos();
        
        if (tableTotalProductos.getSelectedRow()>=0){
            int cantidad = Integer.parseInt(txtCantidad.getText());
            if (cantidad > 0){
                boolean repetido = false;
                for (int i = 0; i < modeloCarrito.getRowCount(); i++){
                    String nprod = tablaCarrito.getValueAt(i,0).toString();
                    if (nprod.equals(n)){
                        int c = Integer.parseInt(modeloCarrito.getValueAt(i, 2).toString());
                        c++;
                        modeloCarrito.setValueAt(c,i,2);
                        repetido = true;
                    }
                }
                
                if (repetido==false){
                    modeloCarrito.addRow(new Object[]{n, p, 1});
                }
                
                txtCantidad.setText(Integer.toString(Integer.parseInt(txtCantidad.getText()) - 1));
                objP.modificarProducto(txtID, txtNombre, txtAncho, txtLargo, txtPrecio, txtImagen, txtCantidad);
                objP.mostrarProductos(tableTotalProductos);
            }
            else {
               JOptionPane.showMessageDialog(null, "Este producto está agotado"); 
            }
        } else{
            JOptionPane.showMessageDialog(null, "Elige algún producto");
        }
        
    }//GEN-LAST:event_btnAñadirActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tablaCarrito.getSelectedRow();
        Clases.CProductos objP = new Clases.CProductos();
        if (fila >= 0) {
            String nomp = modeloCarrito.getValueAt(fila,0).toString();
            for (int i = 0; i < tableTotalProductos.getRowCount(); i++){
                if (nomp.equals(tableTotalProductos.getValueAt(i, 1).toString())){
                    tableTotalProductos.setRowSelectionInterval(i, i);
                    
                    int ca = Integer.parseInt(tableTotalProductos.getValueAt(i, 6).toString());
                    int regresar = Integer.parseInt(modeloCarrito.getValueAt(fila, 2).toString());
                    int t = ca + regresar;
                    
                    txtID.setText(tableTotalProductos.getValueAt(i, 0).toString());
                    txtNombre.setText(tableTotalProductos.getValueAt(i, 1).toString());
                    txtAncho.setText(tableTotalProductos.getValueAt(i, 2).toString());
                    txtLargo.setText(tableTotalProductos.getValueAt(i, 3).toString());
                    txtPrecio.setText(tableTotalProductos.getValueAt(i, 4).toString());
                    txtImagen.setText(tableTotalProductos.getValueAt(i, 5).toString());
                    lblImagen.setIcon(new ImageIcon("imagenes/"+txtImagen.getText()));
                    txtCantidad.setText(Integer.toString(t));
                    
                    objP.modificarProducto(txtID, txtNombre, txtAncho, txtLargo, txtPrecio, txtImagen, txtCantidad);
                    objP.mostrarProductos(tableTotalProductos);
                    
                    modeloCarrito.removeRow(fila);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona un producto del carrito para eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnVaciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVaciarActionPerformed
        if (modeloCarrito.getRowCount()==0){
           JOptionPane.showMessageDialog(null, "El carrito ya esta vacío"); 
        } else {
            while (modeloCarrito.getRowCount() > 0) {
                tablaCarrito.setRowSelectionInterval(0, 0);
                btnEliminar.doClick();
                //modeloCarrito.removeRow(0);
            }
        }
    }//GEN-LAST:event_btnVaciarActionPerformed

    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        total = 0;
        if (modeloCarrito.getRowCount()==0){
           JOptionPane.showMessageDialog(null, "El carrito esta vacío"); 
        } else{
            txtCorreo.setText("@");
            for (int i = 0; i < modeloCarrito.getRowCount(); i++){
                double s = Double.parseDouble(tablaCarrito.getValueAt(i,1).toString());
                double m = Double.parseDouble(tablaCarrito.getValueAt(i,2).toString());

                total = total+(s*m);
            }
            
            for(int i = 0; i < modeloCarrito.getRowCount(); i++){
                list1.add((String) tablaCarrito.getValueAt(i, 0));
                list2.add(Double.valueOf(tablaCarrito.getValueAt(i,1).toString()));
                list3.add(Integer.valueOf(tablaCarrito.getValueAt(i,2).toString()));
            }
            
            txtPagoCon.setText("");
            txtCambio.setText("");
            lblTotal.setText("Total A Pagar: $" + total);
            dialogRecibo.setSize(524, 532);
            dialogRecibo.setLocationRelativeTo(null);
            dialogRecibo.setVisible(true);
        }
    }//GEN-LAST:event_btnComprarActionPerformed

