    private void moverCampo(JTextField campo){
        Point puntoOriginal = campo.getLocation();
        
        new Thread(() -> {
            try{
                for (int i = 0; i < 10; i++){
                    campo.setLocation(puntoOriginal.x + 5, puntoOriginal.y);
                    Thread.sleep(30);
                    campo.setLocation(puntoOriginal.x - 5, puntoOriginal.y);
                    Thread.sleep(30);
                }
                campo.setLocation(puntoOriginal);
            }catch(InterruptedException ex){
                ex.printStackTrace();
            }   
        }).start();
    }

