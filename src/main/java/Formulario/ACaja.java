    private void txtPagoConKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPagoConKeyReleased
        // TODO add your handling code here:
        String texto = txtPagoCon.getText();
        
        if (!texto.matches("\\d*")) {
            txtPagoCon.setText(texto.replaceAll("[^\\d]", ""));
        }
        
        double pagoCon = Integer.parseInt(texto);
        if(pagoCon >= total){
            txtCambio.setText("" + (pagoCon - total));
        } else{
            txtCambio.setText("");
        }
    }//GEN-LAST:event_txtPagoConKeyReleased

